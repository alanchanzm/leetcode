import fn from '../src/034';

test(`${fn.name} --- [5,7,7,8,8,10],8 --- [3,4]`, () => {
  expect(fn([5, 7, 7, 8, 8, 10], 8)).toEqual([3, 4]);
});
test(`${fn.name} --- [5,7,7,8,8,10],6 --- [-1,-1]`, () => {
  expect(fn([5, 7, 7, 8, 8, 10], 6)).toEqual([-1, -1]);
});

test(`${fn.name} --- [2,2],2 --- [0,1]`, () => {
  expect(fn([2, 2], 2)).toEqual([0, 1]);
});
